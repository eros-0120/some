#include<iostream>
using namespace std;

int main() {

	//比较运算符

	// == 相等于
	int a = 10;
	int b = 20;
	cout << (a == b)<<endl;//0代表假，1代表真


	// != 不相等
	cout << (a != b) << endl;


	// >
	cout << (a > b) << endl;


	// <
	cout << (a < b) << endl;


	// >=
	cout << (a >= b) << endl;


	// <=
	cout << (a <= b) << endl;




	system("pause");

	return 0;
}